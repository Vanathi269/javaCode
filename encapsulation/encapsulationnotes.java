package encapsulation;

public class encapsulationnotes {
/*
 * encapsulation:
 * wrapping the data with our methods
 * to hide the data
 * 
 * any class can create an object and can update the data
 * which we don't want
 * 
 * to achieve this, we use access modifier
 * also known as getters and setters
 * 
 * 
 * 
 * access modifiers
 * four types:
 * private-can be accessed only inside the same class
 * default-can be accessed only within the package
 * protected-can be accessed only within the package and outside the package only by the child class 
 * public-can be accessed throughtout the project
 * 
 * 
 */
}
