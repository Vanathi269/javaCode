package classesobjectsdemo;

public class classobjectnotes {
	/*
	 * objects are always stored inside heap memory
	 * object which u see is an object
	 * eg:pen,pencil,laptop,human,etc.....
	 * 
	 * object has 
	 * 1.properties:attributes to identify the object are defined as variables in java  
	 * 2.behaviours:what action performed by that object are defined as methods in java 
	 * 
	 * pen:
	 * properties:color,weight,height,logo,brand,type,grip size,price
	 * behaviour:write,scribble,draw
	 * 
	 * car:
	 * properties:brand,model,weight,color,type
	 * behaviour:drive,accelerate,break,sliding window
	 * 
	 * human being:
	 * properties:height,weight,color,gender,address,blood group
	 * behaviour:walking,eating,running,laughing,sleeping
	 * 
	 * 
	 * 
	 * classes:
	 * is a collection of similar objects
	 * 
	 * students class should contain only student object
	 * all student class has properties and behaviour
	 * 
	 * teacher class should contain only teacher object
	 * 
	 * 
	 * 
	 */

}
