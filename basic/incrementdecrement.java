package basic;

public class incrementdecrement {

	public static void main(String[] args) {
		//increment :pre or post
		//decrement :pre or post
		
		int a=4;
		int b=++a;
		System.out.println(a);
		System.out.println(b);

		int c=10;
		int d=c--;
		System.out.println(c);
		System.out.println(d);
		
	}

}
