package extra;

public class printingnumbers {

	public static void main(String[] args) {
		System.out.println("using for loop");
		for(int i=1;i<=5;i++) {
			System.out.println(i);
		}
		System.out.println("using do while loop");
		int x=1;
		do {
			System.out.println(x);
			x++;
		}while(x<=5);
			
	}
}
